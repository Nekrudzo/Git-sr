using System;
   class App {
    static int Subtract(int a, int b){
        return a - b;
    }

        static int Multiply(int a, int b){
        return a * b;
    }
       static void Main() {
           Subtract(30, 12)
           Multiply(2, 4)
           Console.WriteLine("Калькулятор v2.0-FINAL");
       }
       Console.WriteLine($"Результат: {Subtract}");
       Console.WriteLine($"Результат: {Multiply}");
   }
